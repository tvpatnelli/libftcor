#include "libft.h"

void		ft_putnbr(int n)
{
	long	m;

	m = (long)n;
	if (m < 0)
	{
		ft_putchar('-');
		m = -m;
	}
	if (m > 9)
	{
		ft_putnbr(m / 10);
		ft_putnbr(m % 10);
	}
	else
		ft_putchar(m + 48);
}
